<script>
    export default {
      data() {
        return {
          txtActive: 0,
          txtLength: 0,
          txtDirection: 1,
          txtArray: [ "медиа", "сообщество", "образование" ],
          txtDuration: 100,
        }
      },
      methods: {
        changeTxt() {
          setTimeout(() => {
            if (this.txtDirection > 0) {
              this.txtLength += 1;
            }
            if (this.txtDirection < 0) {
              this.txtLength -= 1;
            }
            if (this.txtLength === this.txtArray[this.txtActive].length + 1) {
              this.txtDirection = -1;
            }
            if (this.txtLength === 0) {
              this.txtDirection = 1;
              this.txtActive += 1;
              this.txtActive %= this.txtArray.length;
            }
            this.txtDuration = Math.random() * 300 + this.txtDirection * 100;
            this.changeTxt();
          }, this.txtDuration)
        }
      },
      computed: {
        txtAnimate() {
          return this.txtArray[this.txtActive].substr(0, this.txtLength);
        }
      },
      mounted() {
        this.changeTxt();
      },
    }
</script>
